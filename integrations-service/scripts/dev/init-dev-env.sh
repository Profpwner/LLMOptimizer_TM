2. **Check PersistentVolumeClaims:**
   ```bash
   kubectl get pvc -n llmoptimizer-dev
   kubectl describe pvc <pvc-name> -n llmoptimizer-dev
   ```

3. **Check pod events:**
   ```bash
   kubectl describe pod <pod-name> -n llmoptimizer-dev
